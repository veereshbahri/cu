package problem1;

public class UserBean {
	
	private String email;
	private String password;
	
	public UserBean(String email, String password) {
		super();
	
		this.email = email;
		this.password = password;
		
	}
	
	public String getEmail() {
		return email;
	}
	public String getPassword() {
		return password;
	}

	

}
